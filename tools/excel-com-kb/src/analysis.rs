fn object_evidence(objects: &[ObjectRecord], id: &str) -> String {
    objects
        .iter()
        .find(|object| object.id == id)
        .map(|object| {
            format!(
                "Documentation-established: {}; source `{}`.",
                sanitize(&object.summary),
                object.source.path
            )
        })
        .unwrap_or_else(|| "Deferred: no canonical object record found.".to_owned())
}

fn member_evidence(members: &[MemberRecord], id: &str) -> String {
    members
        .iter()
        .find(|member| member.id == id)
        .map(member_record_evidence)
        .unwrap_or_else(|| "Deferred: no canonical member record found.".to_owned())
}

fn member_record_evidence(member: &MemberRecord) -> String {
    let return_type = member
        .documented_return_type
        .as_deref()
        .unwrap_or("not retained");
    format!(
        "Documentation-established: {} {}, access `{}`, return `{}`, {} parameters; source `{}`.",
        member.kind,
        sanitize(&member.summary),
        member.access,
        return_type,
        member.parameters.len(),
        member.source.path
    )
}

fn sanitize(value: &str) -> String {
    value.replace('|', "\\|").replace(['\n', '\r'], " ")
}
