//! Deterministic, documentation-only design analysis built from the canonical
//! Excel object-model records. This module never edits `data/`.

use crate::model::{MemberRecord, ObjectRecord, RelationshipRecord, SourceCoverage};
use crate::validate;
use std::collections::BTreeMap;
use std::fs;
use std::path::Path;

const REPORTS: [&str; 6] = [
    "architectural-spine.md",
    "candidate-0.1-members.md",
    "collection-analysis.md",
    "optional-argument-targets.md",
    "typelib-audit-targets.md",
    "runtime-probe-targets.md",
];

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct AnalysisSummary {
    pub reports: usize,
}

pub fn analyze_reports(root: &Path) -> Result<AnalysisSummary, String> {
    validate::validate_root(root)?;
    let reports = report_contents(root)?;
    let directory = root.join("generated").join("analysis");
    fs::create_dir_all(&directory)
        .map_err(|error| format!("cannot create {}: {error}", directory.display()))?;
    for (name, content) in reports {
        fs::write(directory.join(name), content)
            .map_err(|error| format!("cannot write analysis report: {error}"))?;
    }
    Ok(AnalysisSummary {
        reports: REPORTS.len(),
    })
}

pub fn check_reports(root: &Path) -> Result<(), String> {
    let first = report_contents(root)?;
    let second = report_contents(root)?;
    if first != second {
        return Err("analysis report generation is not deterministic".to_owned());
    }
    for (name, expected) in first {
        let path = root.join("generated").join("analysis").join(name);
        let actual = fs::read_to_string(&path)
            .map_err(|error| format!("cannot read analysis report {}: {error}", path.display()))?;
        if actual != expected {
            return Err(format!(
                "analysis report {} is stale; run analyze",
                path.display()
            ));
        }
        if actual.contains("\r\n") || !actual.ends_with('\n') {
            return Err(format!(
                "analysis report {} does not use deterministic LF/final-newline output",
                path.display()
            ));
        }
    }
    Ok(())
}

pub fn report_contents(root: &Path) -> Result<BTreeMap<&'static str, String>, String> {
    let (objects, members, relationships, _enums, coverage) = validate::load_records(root)?;
    let mut reports = BTreeMap::new();
    reports.insert(
        "architectural-spine.md",
        architectural_spine(&objects, &members, &relationships, &coverage),
    );
    reports.insert("candidate-0.1-members.md", candidate_members(&members));
    reports.insert(
        "collection-analysis.md",
        collection_analysis(&objects, &members, &coverage),
    );
    reports.insert(
        "optional-argument-targets.md",
        optional_targets(&members, &relationships),
    );
    reports.insert("typelib-audit-targets.md", typelib_targets(&members));
    reports.insert("runtime-probe-targets.md", runtime_probes(&members));
    Ok(reports)
}

fn heading(title: &str, purpose: &str) -> String {
    format!(
        "# {title}\n\nGenerated by `excel-com-kb analyze`; do not edit by hand. {purpose}\n\nEvidence labels are deliberate: **Documentation-established** is extracted from the pinned VBA documentation; **Project classification** is a planning label; **Type-library verification required** and **Runtime verification required** are open gates. No production API, COM activation, or type-library inspection is performed.\n\n"
    )
}

